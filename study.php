<h1>Chapter 3</h1>
<p>
One of the most prominent cyber attacks is the distributed denial-of-service or DDoS attack that is an attempt to crash a server, website or network by overloading it with traffic.
 DDoS attacks overwhelm enterprise networks via the simple network management protocol, used for modems, printers, switches, routers, and servers.
 A Man-in-the-middle or (MitM) attack is when a cybercriminal intercepts and relays messages between two parties in order to steal data.
 While not much can be done for to avoid this if you are not directly connected to the IT department you should notify someone who is so they can start to deal with the problem.
 Be on the look out for when your computers or servers start to slow down as this may be a symptom of a DDoS attack.
 A MitM may be identified from a suspisious networtks that are open/public, these may have been set up by an attacker to pull data between network transfers.

